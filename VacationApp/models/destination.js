class Destination {
  constructor(id, countryId, name, cost, year, rating, description, imageUrl) {
    this.id = id;
    this.countryId = countryId;
    this.name = name;
    this.cost = cost;
    this.year = year;
    this.rating = rating;
    this.description = description;
    this.imageUrl = imageUrl;
  }
}

export default Destination;